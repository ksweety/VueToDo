<script setup> 
import {ref, watch} from "vue" 
 let filter=ref("all")
 

 let todos= ref(JSON.parse(window.localStorage.getItem("todos")) ?? [])

  watch(todos,function(value) {
    window.localStorage.setItem("todos",JSON.stringify(value))
  }, {deep:true}) 

  function todoFilter (todo) {
    if(filter.value=="active") {
      return todo.complete== false

    }
    else if(filter.value=="completed") {
      return todo.complete== true
    }
    else {
      return true
    }
  }

 let newtodo= ref("")

 function clearTodos(){
  todos.value = []
 }
 
 
 function popUpTodo () {
  todos.value.push({
    text: newtodo.value, 
    complete: false 
  })

  newtodo.value=""

 } 
 function deleteTodo(index) { 
  todos.value.splice(index,1) 

 }

 function activeFilter (todo) {
    return todo.complete == false
  }
</script>

<template> 
  <p v-if="todos.length> 0">
  </p>
  <p>{{ todos.filter(activeFilter).length }} items left</p>
  <label class="form-control"><input name="filter" type="radio" value="all" v-model="filter">All</label>
  <label class="form-control"><input name="filter" type="radio" value="active" v-model="filter">Active</label> 
  <label class="form-control"><input name="filter"  type="radio" value="completed" v-model="filter">Completed</label>

  <h1>My Todo List</h1>    
 

  <ul class="dripster">
  <li v-for="(todo, index) in todos.filter(todoFilter)">
    <label class="container"><input type="checkbox" class="boxdrip" v-model="todo.complete"><span class="checkmark"></span></label>
    <button @click="deleteTodo(index)" class="dripbutton">‚ùå</button> 
   


    <span :class="{completed: todo.complete}">{{ todo.text }}</span>

 
  </li> 
  </ul> 
  
  <input v-model="newtodo" class="button " @keydown.enter="popUpTodo">
  <p><button @click="popUpTodo" class="button">Add Todo</button> 
    <button @click="clearTodos" class="button">Clear All</button>
  </p> 

   
</template>

